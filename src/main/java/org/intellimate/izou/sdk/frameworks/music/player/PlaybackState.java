package org.intellimate.izou.sdk.frameworks.music.player;

/**
 * this class represents the 2 different PlaybackStates
 * @author LeanderK
 * @version 1.0
 */
public enum PlaybackState {
    /**
     * indicates that the Player is playing
     */
    PLAY,
    /**
     * indicates that the Player is pausing, but has not ended
     */
    PAUSE
}
